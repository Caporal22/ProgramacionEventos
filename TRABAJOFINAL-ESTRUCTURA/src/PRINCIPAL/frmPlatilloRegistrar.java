    private void jbtModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtModificarActionPerformed
        try {
        
        String nombre = jtfNombre.getText().trim();
        String categoria = jtfCategoria.getText().trim();
        String precioTexto = jtfPrecio.getText().trim();
        String horarioDisponible = jtfHorarioDisponible.getText().trim();

       
        if (nombre.isEmpty() || categoria.isEmpty() || precioTexto.isEmpty() || horarioDisponible.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        double precio;
        try {
            precio = Double.parseDouble(precioTexto);
            if (precio <= 0) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese un precio vÃ¡lido mayor a 0.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
       
        NodoPlatillo actual = colaPlatillos.getFrente();
        boolean encontrado = false;

        while (actual != null) {
            Platillo platillo = actual.getPlatillo();
   
            if (platillo.getNombre().equalsIgnoreCase(nombre)) {
               
                platillo.setCategoria(categoria);
                platillo.setPrecio(precio);
                platillo.setHorarioDisponible(horarioDisponible);

                javax.swing.JOptionPane.showMessageDialog(this, "Platillo modificado exitosamente.", "Ãxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                encontrado = true;
                break;
            }
            actual = actual.getSiguiente();
        }

        if (!encontrado) {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontrÃ³ un platillo con ese nombre.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

        jtfNombre.setText("");
        jtfCategoria.setText("");
        jtfPrecio.setText("");
        jtfHorarioDisponible.setText("");

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "OcurriÃ³ un error inesperado: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jbtModificarActionPerformed


